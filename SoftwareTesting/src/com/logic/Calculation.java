package com.logic;

public class Calculation 
{
		    public static int findMax(int arr[])//1,2,3,4
		    {  
		    	int max=arr[0];//1
		    	for(int i=0;i<arr.length;i++)//1,2
		    	{
		    		if(max<arr[i])//1<1 1<2 2<3 3<4
		    		{
		    			max=arr[i];//max=2 max=3 max=4
		    		}
		    	}
		    	return max;//4
		    }


}
