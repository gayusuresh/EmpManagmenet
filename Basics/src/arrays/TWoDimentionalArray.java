package arrays;

public class TWoDimentionalArray {

	public static void main(String[] args) {
		int arr[][]=new int[2][2];
		arr[0][0]=12;
		arr[0][1]=13;
		arr[1][0]=14;
		arr[1][1]=15;
		System.out.print(arr[0][0]+" ");
		System.out.println(arr[0][1]);
		System.out.print(arr[1][0]+" ");
		System.out.print(arr[1][1]);
		
	}

}
