package variablesanddatatypes;

public class BitWiseAndExample {

	public static void main(String[] args) {
		int x=12,y=18;
		int z= x&y;
		int z1= x|y;
		int k=x^y;
		
		
		System.out.println(z);
		System.out.println(z1);
		System.out.println(k);
		int c=42;
		int b=~c;
		System.out.println(b);
	}

}
