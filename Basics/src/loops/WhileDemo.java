package loops;
/*  WAP to print 1 to 100*/
public class WhileDemo {

	public static void main(String[] args) {
		int i=1;//intialization
		while(i>=100)//condition check  1<=100 2<=100 3<=100 4<=100---100<=100
			//101<=100=fasle
		{
			System.out.println(i);//1,2,3,4---100
			i++;//incrementation i=1+1=2 ,3,4,101
		}

	}

}
